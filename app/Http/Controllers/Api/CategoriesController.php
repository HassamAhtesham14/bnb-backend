     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'slug' => 'required',
            'image' => 'required'
        ]);

        $cartItem = Category::create($request->only(['name', 'slug', 'image']));

        return response()->json(compact('cartItem'));
    }

